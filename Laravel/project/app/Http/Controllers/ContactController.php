    {
           $validator=$request->validate([
            'name' => 'required|alpha',
            'email' => 'required|unique:customers',
            'comment'=>'required'
        ]);
        $contact=new contact;
        $contact->name=$request->name;
        $contact->email=$request->email;
        $contact->comment=$request->comment;
        $res=$contact->save();
        if($res)
        {
            echo "<script> 
                alert('contact Success'); 
                window.location='/contact';
                </script>";
        }
    }

